        await action_blocks.handleMyEnergyApiCallFailure(
          context,
          wwwAuthenticateHeader: (_model.topupPreferencesGetOutput
                  ?.getHeader('www-authenticate') ??
              ''),
          httpStatusCode: (_model.topupPreferencesGetOutput?.statusCode ?? 200),
        );
