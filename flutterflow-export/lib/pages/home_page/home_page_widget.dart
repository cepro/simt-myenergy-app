              (FFAppState().properties.first.owner == FFAppState().customerId);
          _model.inPrepayMode = functions.isPrepayMode(_model.supplyMeter);
        });
        setState(() {
          FFAppState().supplyContractSigned = functions
                      .getContractByType(
                          FFAppState().accounts.toList(), 'supply')
                      ?.signedDate !=
                  null &&
