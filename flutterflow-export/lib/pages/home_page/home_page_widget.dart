          ((_model.homePageGetWallets?.jsonBody ?? '') != null)) {
        setState(() {
          _model.singleWalletBalance =
              functions.formatCurrencyAmount(getJsonField(
            (_model.homePageGetWallets?.jsonBody ?? ''),
